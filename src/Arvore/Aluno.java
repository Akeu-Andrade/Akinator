package Arvore;


public class Aluno {
    private String nome;
    private int cpf, marticula;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public int getMarticula() {
        return marticula;
    }

    public void setMarticula(int marticula) {
        this.marticula = marticula;
    }
    
    
}
